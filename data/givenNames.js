// Automatically adds vaguely convincing frequency data to a list without frequency data
function autofrequency (array)
{
	var count = 0.0 + array.length;

	for (var i in array)
	{
		var linear = 1 + ((count + 1 - i) / count) * 5;
		var power = Math.pow(3, linear);
		array[i].frequency = Math.floor(power);
	}
}

